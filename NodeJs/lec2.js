//Node commands:

//To check Node version
//node -v  

//To check NPM version
//npm -v   

//To run a JS file using Node
//node filename.js

//To initialize a Node project and create package.json
//npm init

//To install a package and add it to package.json
//npm install package_name

//To install a package globally
//npm install -g package_name

//To uninstall a package
//npm uninstall package_name

//To update a package
//npm update package_name

//To list installed packages
//npm list

//To run a script defined in package.json
//npm run script_name

//To create a package-lock.json file    
//npm install (if package-lock.json is not present)

//To view outdated packages
//npm outdated

//To view help for npm commands
//npm help command_name

//To clear npm cache
//npm cache clean --force

//To view globally installed packages
//npm list -g --depth=0

//To create a new Node project with default settings
//npm init -y   

//To install multiple packages at once
//npm install package1 package2 package3

//To install a specific version of a package
//npm install package_name@version_number

//To update npm to the latest version
//npm install -g npm@latest

//To view the details of a package
//npm view package_name 

//To watch the changes in a file
//nodemon filename.js  (requires nodemon package)

